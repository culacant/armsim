DRAW_MAP
	adr r12, =player_pos
	ldr r9, [r12], #4		; posx
	ldr r10, [r12], #4		; posy

	add r5, r9, #32			; Ix
	mov r5, r5 lsr #6		; mask out 64 for tile index
	add r6, r5, #6			; Fx
	subs r5, r5, #6			; skip negative tiles
	movmi r5, #0
	mov r4, r5				; Cx

	add r7, r10, #64 		; Iy = Cy
	mov r7, r7 lsr #6		; mask out 64 for tile index
	add r8, r7, #6			; Fy
	subs r7, r7, #6			; skip negative tiles
	movmi r7, #0

	adr r12, =player_ofs
	ldr r1, [r12], #4		; ofsx
	ldr r2, [r12], #4		; ofsy
	sub r9, r9, r1			; total ofs
	sub r10, r10, r2
	
	adr r12, =tile_rows		; # rows
	ldr r12, [r12]
	cmp r7, r12
	movge r7, r12

	adr r12, =tile_cols		; # cols
	ldr r12, [r12]
	cmp r6, r12
	movge r6, r12

	mov r11, #64			; sprite size
	mov r3, #0				; bg index

draw_map_row
	mla r2, r7, r12, r4		; tile adress ofs

	adr r1, =map_tiles
	add r1, r1, r2
	ldrb r1, [r1]			; tile #
	
	adr r2, =tile_struct
	add r2, r2, r1, lsl #2	; tile = 4 bytes
	ldrb r0, [r2]			; first byte is sprite #

	mul r1, r4, r11
	sub r1, r1, r9			; subtract ofs
	mul r2, r7, r11
	sub r2, r2, r10			; subtract ofs
	
	swi swi_drawsprite_bg
	add r3, r3, #1			; increment bg index
	add r4, r4, #1			; Cx
	cmp r4, r6
	bne draw_map_row

	add r7, r7, #1			; Cy
	mov r4, r5				; reset Cx
	cmp r7, r8				; Cy = Fy?
	bne draw_map_row

	mov pc, lr

MOVE_PLAYER
; r0: inputs
; r1: posx
; r2: posy
; r3: pos adr
; r4: movement x
; r5: movement y
; r6: movement speed
	mov r4, #0
	mov r5, #0
	mov r6, #5			; movement speed

	tst r0, #3			; mask: 0011
	tstne r0, #12		; mask: 1100
	movne r6, #4		; diagonal speed = 4/5ths normal

	tst r0, #1			; KEY_UP
	subne r5, r5, r6
	tst r0, #2			; KEY_DWN
	addne r5, r5, r6

	tst r0, #8			; KEY_RIGHT
	addne r4, r4, r6
	tst r0, #4			; KEY_LEFT
	subne r4, r4, r6

	adr r3, =player_pos
	ldr r1, [r3], #4		; posx
	ldr r2, [r3], #4		; posy

	add r1, r1, r4
	stmib sp!, {r1-r5, lr}
	bl COLLIDE_TERRAIN
	ldmda sp!, {r1-r5, lr}
	cmp r0, #0
	subeq r1, r1, r4

	add r2, r2, r5
	stmib sp!, {r1-r5, lr}
	bl COLLIDE_TERRAIN
	ldmda sp!, {r1-r5, lr}
	cmp r0, #0
	subeq r2, r2, r5

	adr r3, =player_pos
	str r1, [r3], #4		; posx
	str r2, [r3], #4		; posy

	mov pc, lr

COLLIDE_TERRAIN
; r1: posx
; r2: posy
	add r4, r1, #32			; ofs: 32px
	mov r4, r4 lsr #6		; mask out 64 for tile index
	add r5, r2, #64 		; ofs: 64px
	mov r5, r5 lsr #6

	adr r3, =tile_cols
	ldr r3, [r3]			; cols
	mla r0, r5, r3, r4		; y*cols + x; ofs in r0
	
	adr r3, =map_tiles
	add r3, r3, r0
	ldrb r4, [r3]			; tile #

	adr r3, =tile_struct		
	add r3, r3, r4, lsl #2	; 2nd byte = walkable
	ldrb r0, [r3, #1]		; walkable in r0

	mov pc, lr

DRAW_PLAYER
;r0: sprite nr
;r1: posx
;r2: posy
;r3: adress/drawable
	mov r0, #1				; spritenr
	adr r3, =player_ofs
	ldr r1, [r3], #4		; posx
	ldr r2, [r3], #4		; posx
	mov r3, #0				; drawable
	swi swi_drawsprite_fg
	mov pc, lr

